package com.webapp;

public class Watch 
{
	private int id;
	private String brand;
	private int price;
	
	public void setId(int i)
	{
		id=i;
	}
	public int getId()
	{
		return id;
	}
	public void setBrand(String b)
	{
		brand=b;
	}
	public String getBrand()
	{
		return brand;
	}
	public void setPrice(int p)
	{
		price=p;
	}
	public int getPrice()
	{
		return price;
	}
}
